#🧪 Task 1 (easy)

#👉 Start at 2
#👉 Add 2 each time
#👉 Stop when number is greater than 10
#👉 Print values

number = 2
while number <= 10:
    print(number)
    number = number + 2

#🧪 Task 2 (reverse)

#👉 Start at 7
#👉 Subtract 2
#👉 Stop when number is less than 1
#👉 Print values

number = 7
while number >= 1:
    print(number)
    number = number - 2
